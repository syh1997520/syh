package com.qlgshopping.entity;
/**
 * 我的浏览历史的实体类
 * @author Administrator
 *
 */
public class MyHistory {
private int hisId;
private int hisFromId;
private int hisForId;
private String hisDate;
private int hisDelete;
private UserInfo user;
private Product product;

public UserInfo getUser() {
	return user;
}
public void setUser(UserInfo user) {
	this.user = user;
}
public Product getProduct() {
	return product;
}
public void setProduct(Product product) {
	this.product = product;
}
public int getHisId() {
	return hisId;
}
public void setHisId(int hisId) {
	this.hisId = hisId;
}
public int getHisFromId() {
	return hisFromId;
}
public void setHisFromId(int hisFromId) {
	this.hisFromId = hisFromId;
}
public int getHisForId() {
	return hisForId;
}
public void setHisForId(int hisForId) {
	this.hisForId = hisForId;
}
public String getHisDate() {
	return hisDate;
}
public void setHisDate(String hisDate) {
	this.hisDate = hisDate;
}
public int getHisDelete() {
	return hisDelete;
}
public void setHisDelete(int hisDelete) {
	this.hisDelete = hisDelete;
}
@Override
public String toString() {
	return "MyHistory [hisId=" + hisId + ", hisFromId=" + hisFromId
			+ ", hisForId=" + hisForId + ", hisDate=" + hisDate
			+ ", hisDelete=" + hisDelete + "]";
}

}
