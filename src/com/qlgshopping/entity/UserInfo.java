package com.qlgshopping.entity;

import java.util.ArrayList;

/**
 * 用户类
 * @author Administrator
 *
 */
public class UserInfo {
	private int userId;//用户编号
	private String userPassword;//密码  6-12位
	private String userLoginName;//登录时的用户名（6-12位）,由英文构成  
	private String userGender;//   男或女
	private String userName;//用户登陆以后设置的用户名3-10个字，不可重复
	private String userPhoneNumber;//用户手机号
	private int userScore;//用户的积分，默认为0
	private String userimg;//用户头像，url路径
	private int speakAble;
	private ArrayList<Evalute> evaluteList;//代表用户的所有评论集合
	private ArrayList<OrderTable> orderTableList;//代表用户所下订单的集合
	private ArrayList<ReceiveAddress> receiveAddress;//代表用户的收货地址集合
	private ShoppingCart shoppingCart;//用户的购物车
	private ArrayList<EvaLike> likeList;
	private ArrayList<MyHistory> historyList;
	
	public ArrayList<MyHistory> getHistoryList() {
		return historyList;
	}


	public void setHistoryList(ArrayList<MyHistory> historyList) {
		this.historyList = historyList;
	}


	public ArrayList<EvaLike> getLikeList() {
		return likeList;
	}


	public void setLikeList(ArrayList<EvaLike> likeList) {
		this.likeList = likeList;
	}


	public int getUserId() {
		return userId;
	}


	public void setUserId(int userId) {
		this.userId = userId;
	}


	public String getUserPassword() {
		return userPassword;
	}


	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}


	public String getUserLoginName() {
		return userLoginName;
	}


	public void setUserLoginName(String userLoginName) {
		this.userLoginName = userLoginName;
	}


	public String getUserGender() {
		return userGender;
	}


	public void setUserGender(String userGender) {
		this.userGender = userGender;
	}


	public String getUserName() {
		return userName;
	}


	public void setUserName(String userName) {
		this.userName = userName;
	}


	public String getUserPhoneNumber() {
		return userPhoneNumber;
	}


	public void setUserPhoneNumber(String userPhoneNumber) {
		this.userPhoneNumber = userPhoneNumber;
	}


	public int getUserScore() {
		return userScore;
	}


	public void setUserScore(int userScore) {
		this.userScore = userScore;
	}


	public String getUserimg() {
		return userimg;
	}


	public void setUserimg(String userimg) {
		this.userimg = userimg;
	}

	
	public ArrayList<Evalute> getEvaluteList() {
		return evaluteList;
	}


	public void setEvaluteList(ArrayList<Evalute> evaluteList) {
		this.evaluteList = evaluteList;
	}
	

	public ArrayList<OrderTable> getOrderTableList() {
		return orderTableList;
	}


	public void setOrderTableList(ArrayList<OrderTable> orderTableList) {
		this.orderTableList = orderTableList;
	}

	
	public ArrayList<ReceiveAddress> getReceiveAddress() {
		return receiveAddress;
	}


	public void setReceiveAddress(ArrayList<ReceiveAddress> receiveAddress) {
		this.receiveAddress = receiveAddress;
	}
	

	public ShoppingCart getShoppingCart() {
		return shoppingCart;
	}


	public void setShoppingCart(ShoppingCart shoppingCart) {
		this.shoppingCart = shoppingCart;
	}


	public int getSpeakAble() {
		return speakAble;
	}


	public void setSpeakAble(int speakAble) {
		this.speakAble = speakAble;
	}

	
	

}
